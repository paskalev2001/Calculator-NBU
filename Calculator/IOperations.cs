namespace Calculator.Core
{
    // Интерфейс, който описва общия договор за всички операции в калкулатора.
    // Всеки клас, представляващ математическа операция, трябва да го имплементира.
    public interface IOperation
    {
        // Уникален текстов символ на операцията (напр. "+", "*", "sqrt", "log").
        // Използва се за разпознаване и извикване на правилната операция.
        string Symbol { get; }

        // Определя дали операцията е унарна (true) или бинарна (false).
        // Унарните операции използват само един аргумент.
        bool IsUnary { get; }

        // Метод за изпълнение на операцията.
        // left – първи аргумент (за унарни операции се игнорира).
        // right – втори или единствен аргумент (за унарни операции).
        double Execute(double left, double right);
    }
}
